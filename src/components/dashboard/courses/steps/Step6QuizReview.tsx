'use client';

import React, { useState } from 'react';
import { Sparkles } from 'lucide-react';
import { generateSingleQuestion } from '@/app/actions/quiz-ai';
import { Button } from '@/components/ui/button';
import { QuizQuestion } from '@/types/quiz';
import { CourseWizardData } from '@/types/course';
import { logger } from '@/lib/logger';

// QuizQuestion is now imported from '@/types/quiz'

interface Step6QuizReviewProps {
  data: CourseWizardData;
  quiz?: QuizQuestion[];
  courseId?: string;
  rawContext?: string;
  onQuizUpdate: (quiz: QuizQuestion[]) => void;
}

const formInputClass =
  'w-full px-3 py-2.5 border border-border rounded-lg text-sm text-foreground outline-none transition-colors focus:border-primary focus:ring-[3px] focus:ring-primary/10';
const formLabelClass = 'block text-sm font-semibold text-text-secondary mb-2';

export default function Step6QuizReview({
  data,
  quiz,
  courseId,
  rawContext,
  onQuizUpdate,
}: Step6QuizReviewProps) {
  const questions = quiz || [];
  const [isAdding, setIsAdding] = useState(false);
  const [isGenerating, setIsGenerating] = useState(false);
  const [editingIndex, setEditingIndex] = useState<number | null>(null);
  const [editingQuestion, setEditingQuestion] = useState<QuizQuestion | null>(null);

  const [newQuestion, setNewQuestion] = useState<QuizQuestion>({
    question: '',
    options: ['', '', '', ''],
    answer: 0,
    type: 'multiple_choice',
  });

  const handleAddQuestion = () => {
    if (!newQuestion.question.trim() || newQuestion.options.some((o) => !o.trim())) {
      alert('Please fill in all fields.');
      return;
    }
    const updatedQuiz = [...questions, newQuestion];
    onQuizUpdate(updatedQuiz);
    setIsAdding(false);
    setNewQuestion({ question: '', options: ['', '', '', ''], answer: 0, type: 'multiple_choice' });
  };

  const updateOption = (index: number, value: string) => {
    const newOptions = [...newQuestion.options];
    newOptions[index] = value;
    setNewQuestion({ ...newQuestion, options: newOptions });
  };

  const handleGenerateQuestion = async () => {
    // Find courseId from data if not passed directly
    const targetCourseId = courseId;
    // Fall back to title + description if rawContext isn't fully available yet
    const context =
      rawContext?.trim() || `${data?.title || ''}\n${data?.description || ''}`.trim() || '';

    if (!targetCourseId && !context) {
      alert('Cannot generate a question right now. The course may not be fully saved yet.');
      return;
    }

    try {
      setIsGenerating(true);
      const res = await generateSingleQuestion({ courseId: targetCourseId, context });
      if (res.success && res.question) {
        setNewQuestion({
          question: res.question.question,
          options: res.question.options,
          answer: res.question.answer,
          type: res.question.type,
        });
      } else {
        alert(res.error || 'Failed to generate question with AI.');
      }
    } catch (error) {
      logger.error({ msg: 'Failed to call AI generation:', err: error });
      alert('An unexpected error occurred.');
    } finally {
      setIsGenerating(false);
    }
  };

  // Count archetypes for summary
  const archetypeCounts: Record<string, number> = {};
  questions.forEach((q) => {
    if (q.archetype) {
      archetypeCounts[q.archetype] = (archetypeCounts[q.archetype] || 0) + 1;
    }
  });

  return (
    <div className="relative z-50 flex w-full min-h-0 max-w-[800px] flex-1 flex-col items-center">
      <h2 className="mb-5 shrink-0 text-center text-[32px] font-bold tracking-[-0.5px] text-foreground">
        Review Quiz Questions
      </h2>
      <p className="mb-[30px] max-w-[600px] shrink-0 text-center text-base leading-relaxed text-text-secondary">
        Review the quiz questions generated by AI or add your own.
      </p>

      <div className="flex h-0 w-full min-h-0 flex-[1_1_auto] flex-col overflow-y-auto pr-2 pb-10">
        {/* Header Row */}
        <div className="mb-6 flex shrink-0 items-center justify-between">
          <div className="flex flex-col">
            <div className="mb-1 text-lg font-bold text-foreground">Editable quiz questions</div>
            <div className="text-sm text-text-secondary">
              {questions.length} Questions Generated
            </div>
          </div>
        </div>

        {/* Flat Question List */}
        <div className="flex flex-col gap-5">
          {questions.length === 0 ? (
            <div className="rounded-xl border border-dashed border-border bg-background-secondary p-10 text-center italic text-text-tertiary">
              No questions available. Add one below.
            </div>
          ) : (
            questions.map((q, index) => {
              const isEditing = editingIndex === index;
              if (isEditing && editingQuestion) {
                return (
                  <div key={index} className="mb-8 w-full">
                    <h4 className="m-0 mb-4 text-base">Edit Question {index + 1}</h4>

                    <div className="mb-5">
                      <label className={formLabelClass}>Question Text</label>
                      <input
                        type="text"
                        className={formInputClass}
                        value={editingQuestion.question}
                        onChange={(e) =>
                          setEditingQuestion({ ...editingQuestion, question: e.target.value })
                        }
                      />
                    </div>

                    <div className="mb-5">
                      <label className={formLabelClass}>Options (Select correct answer)</label>
                      <div className="flex flex-col gap-3">
                        {editingQuestion.options.map((opt, i) => (
                          <div key={i} className="flex items-center gap-3">
                            <input
                              type="radio"
                              name={`editCorrectAnswer-${index}`}
                              checked={editingQuestion.answer === i}
                              onChange={() => setEditingQuestion({ ...editingQuestion, answer: i })}
                            />
                            <input
                              type="text"
                              className={formInputClass}
                              value={opt}
                              onChange={(e) => {
                                const newOptions = [...editingQuestion.options];
                                newOptions[i] = e.target.value;
                                setEditingQuestion({ ...editingQuestion, options: newOptions });
                              }}
                              disabled={editingQuestion.type === 'true_false'}
                            />
                          </div>
                        ))}
                      </div>
                    </div>

                    <div className="mt-6 flex justify-end gap-3">
                      <Button
                        variant="ghost"
                        size="sm"
                        onClick={() => {
                          setEditingIndex(null);
                          setEditingQuestion(null);
                        }}
                      >
                        Cancel
                      </Button>
                      <Button
                        variant="default"
                        size="sm"
                        onClick={() => {
                          if (
                            !editingQuestion.question.trim() ||
                            editingQuestion.options.some((o) => !o.trim())
                          ) {
                            alert('Please fill in all fields.');
                            return;
                          }
                          const updatedQuiz = [...questions];
                          updatedQuiz[index] = editingQuestion;
                          onQuizUpdate(updatedQuiz);
                          setEditingIndex(null);
                          setEditingQuestion(null);
                        }}
                      >
                        Save Changes
                      </Button>
                    </div>
                  </div>
                );
              }

              return (
                <div key={index} className="mb-8 w-full">
                  <div className="mb-5 flex items-start justify-between">
                    <div className="flex-1 pr-4 text-base font-semibold leading-relaxed text-foreground">
                      <span className="font-bold mr-2">{index + 1}.</span>
                      {q.question}
                    </div>
                    <div className="flex gap-1.5">
                      <Button
                        variant="outline"
                        size="xs"
                        onClick={() => {
                          setEditingIndex(index);
                          setEditingQuestion(q);
                        }}
                      >
                        Edit
                      </Button>
                    </div>
                  </div>
                  <div className="flex flex-col gap-2.5 pl-6">
                    {q.options.map((opt, optIndex) => (
                      <div
                        key={optIndex}
                        className="flex items-center rounded-lg border border-transparent px-3 py-2 text-sm text-text-secondary transition-colors hover:bg-background-secondary"
                      >
                        <div
                          className={`mr-3 h-[18px] w-[18px] shrink-0 rounded-full border-2 ${
                            q.answer === optIndex
                              ? 'border-success bg-success shadow-[inset_0_0_0_3px_white]'
                              : 'border-[#cbd5e0]'
                          }`}
                        />
                        {opt}
                        {q.answer === optIndex && (
                          <span className="ml-2 text-xs text-success font-semibold">(Correct)</span>
                        )}
                      </div>
                    ))}
                  </div>

                  {/* Embedded Explanation (Always Visible) */}
                  {q.explanation && (
                    <div className="mt-3 px-4 py-3 bg-[#F7FAFC] rounded-lg border border-[#E2E8F0] text-[13px] leading-relaxed">
                      <div className="font-semibold text-[#38A169] mb-1.5 flex items-start gap-1.5">
                        <span>✓</span>
                        <span>Correct: {q.explanation.correctExplanation}</span>
                      </div>
                      {q.explanation.incorrectOptions &&
                        Object.entries(q.explanation.incorrectOptions).map(([key, text]) => (
                          <div key={key} className="text-red-600 mt-2 flex items-start gap-1.5">
                            <span>✕</span>
                            <span>
                              Option {String.fromCharCode(65 + parseInt(key))}: {text}
                            </span>
                          </div>
                        ))}
                      {q.evidence?.moduleSectionHeading && (
                        <div className="text-slate-400 mt-2 text-[11px]">
                          Source: {q.evidence.moduleSectionHeading}
                        </div>
                      )}
                    </div>
                  )}
                </div>
              );
            })
          )}
        </div>

        {/* Add Question Section */}
        {!isAdding ? (
          <Button variant="outline" onClick={() => setIsAdding(true)} className="w-full">
            + Add New Question
          </Button>
        ) : (
          <div className="mt-6 w-full">
            <h3 className="mb-5 text-lg font-bold text-foreground">Add New Question</h3>

            <div className="mb-5">
              <label className={formLabelClass}>Question Text</label>
              <input
                type="text"
                className={formInputClass}
                placeholder="Enter your question here..."
                value={newQuestion.question}
                onChange={(e) => setNewQuestion({ ...newQuestion, question: e.target.value })}
              />
            </div>

            <div className="mb-5">
              <label className={formLabelClass}>Options (Select correct answer)</label>
              <div className="flex flex-col gap-3">
                {newQuestion.options.map((opt, i) => (
                  <div key={i} className="flex items-center gap-3">
                    <input
                      type="radio"
                      name="correctAnswer"
                      checked={newQuestion.answer === i}
                      onChange={() => setNewQuestion({ ...newQuestion, answer: i })}
                    />
                    <input
                      type="text"
                      className={formInputClass}
                      value={opt}
                      onChange={(e) => updateOption(i, e.target.value)}
                      placeholder={`Option ${i + 1}`}
                      disabled={newQuestion.type === 'true_false'}
                    />
                  </div>
                ))}
              </div>
            </div>

            <div className="mt-6 flex items-center justify-between gap-3">
              <div>
                <Button
                  variant="outline"
                  onClick={(e) => {
                    e.preventDefault();
                    handleGenerateQuestion();
                  }}
                  disabled={isGenerating}
                >
                  {isGenerating ? (
                    'Generating...'
                  ) : (
                    <>
                      <Sparkles className="mr-2 size-3.5" />
                      Generate with AI
                    </>
                  )}
                </Button>
              </div>
              <div className="flex gap-3">
                <Button variant="ghost" onClick={() => setIsAdding(false)}>
                  Cancel
                </Button>
                <Button variant="default" onClick={handleAddQuestion}>
                  Save Question
                </Button>
              </div>
            </div>
          </div>
        )}
      </div>
    </div>
  );
}
